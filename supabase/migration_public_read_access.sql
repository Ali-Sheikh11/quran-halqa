-- إزالة قيد "يجب تسجيل الدخول" عن قراءة جدول الطلاب،
-- والسماح لأي زائر (anon) أو مستخدم مسجّل دخوله بمشاهدة الطلاب
-- والنقاط والترتيب والإحصائيات بدون أي قيد.
drop policy if exists "أي مستخدم مسجل دخوله يقرأ الطلاب" on public.students;
drop policy if exists "الجميع يقرأ الطلاب بدون تسجيل دخول" on public.students;
create policy "الجميع يقرأ الطلاب بدون تسجيل دخول"
  on public.students for select
  to anon, authenticated
  using (true);

-- ملاحظة: سياسات الإضافة/التعديل/الحذف (admin فقط) وسياسات صور الطلاب
-- في storage.objects تبقى كما هي بدون أي تغيير — العرض العام لا يعني
-- صلاحية التعديل، التي تبقى حكرًا على المسؤول فقط عبر public.is_admin().
